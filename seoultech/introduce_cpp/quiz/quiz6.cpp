#include<bits/stdc++.h>
using namespace std;

int main(){
  ios::sync_with_stdio(0);
  cin.tie(0);

  int n;
  cout<<(n=4)<<"\n";
  cout<<(n==4)<<"\n";
  cout<<(n>3)<<"\n";
  cout<<(n<4)<<"\n";
  cout<<(n=0)<<"\n";
  cout<<(n==0)<<"\n";
  cout<<(n>0)<<"\n";
  cout<<(n&&4)<<"\n";
  cout<<(n||4)<<"\n";
  cout<<(!n)<<"\n";
}

